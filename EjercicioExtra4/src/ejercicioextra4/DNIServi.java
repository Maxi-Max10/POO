
package ejercicioextra4;


public class DNIServi {
    private int dni;
    private char letra;

    public DNIServi() {
    }
    

    public DNIServi(int dni, char letra) {
        this.dni = dni;
        this.letra = letra;
    }

    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public char getLetra() {
        return letra;
    }

    public void setLetra(char letra) {
        this.letra = letra;
    }
    
    

}
