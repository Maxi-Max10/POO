
package ejercicioextra5;


public class mesServi {
    private String mesUs;
    private String [] mes;

    public mesServi() {
    }

    public mesServi(String mesUs, String[] mes) {
        this.mesUs = mesUs;
        this.mes = mes;
    }

    public String getMesUs() {
        return mesUs;
    }

    public void setMesUs(String mesUs) {
        this.mesUs = mesUs;
    }

    public String[] getMes() {
        return mes;
    }

    public void setMes(String[] mes) {
        this.mes = mes;
    }
    
    

}
